function eub(s) {
  // 使用数组的 map 方法获取每个字符的 Unicode 码点
  // 然后使用 toString 方法将码点转换为至少4位的十六进制数
  var encoded = Array.from(s)
    .map(function (char) {
      return ("0000" + char.charCodeAt(0).toString(16)).slice(-4);
    })
    .join("");
  return encoded;
}
